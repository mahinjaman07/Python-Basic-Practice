numInt = 100;
numFloat = 0.100;
myStr = "100";

# int to float
iTf = float(numInt);
print(iTf)
print(type(iTf));


# Float to int

fTi = int(numFloat);
print(fTi);
print(type(fTi));

# str to int

sTi = int(myStr);
print(sTi);
print(type(sTi));

# str to float

sTf = float(myStr);
print(sTf);
print(type(sTf))