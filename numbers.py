# Python Numbers

'''
There are three numeric types in Python:

int
float
complex
'''

# int

x = 124;

# float

y = 0.100;

# complex

z = 100j;


# type int

i1 = 1
i2 = 35656222554887711
i3 = -3255522

print(type(i1))
print(type(i2))
print(type(i3))

# type float

f1 = 1.10
f2 = 1.0
f3 = -35.59

print(type(f1))
print(type(f2))
print(type(f3))


# type complex

c1 = 3+5j
c2 = 5j
c3 = -5j

print(type(c1))
print(type(c2))
print(type(c3))